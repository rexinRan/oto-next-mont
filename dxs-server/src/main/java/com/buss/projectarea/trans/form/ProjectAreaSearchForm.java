package com.buss.projectarea.trans.form;

import com.buss.projectarea.entity.ProjectAreaEntity;
import com.buss.quiz.entity.QuizEntity;
import org.jweb.core.bean.SearchForm;
import org.jweb.core.bean.annotation.Mapping;


/**
 * @author lu
 * @date 2016-06-13 15:25:09
 * @version V1.0
 *
 */
@Mapping(entityClass=ProjectAreaEntity.class)
public class ProjectAreaSearchForm extends SearchForm {
    /**主键*/
    @Mapping
    private String id;
    /**领域名称*/
    @Mapping
    private String areaName;
    /**编号*/
    @Mapping
    private Integer sort;
    /**创建时间*/
    @Mapping
    private String createTime;
    /**更新时间*/
    @Mapping
    private String updateTime;
    /**删除标志*/
    @Mapping
    private String isDelete;

    public ProjectAreaSearchForm() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getAreaName() {
        return areaName;
    }

    public void setAreaName(String areaName) {
        this.areaName = areaName;
    }

    public Integer getSort() {
        return sort;
    }

    public void setSort(Integer sort) {
        this.sort = sort;
    }

    public String getCreateTime() {
        return createTime;
    }

    public void setCreateTime(String createTime) {
        this.createTime = createTime;
    }

    public String getUpdateTime() {
        return updateTime;
    }

    public void setUpdateTime(String updateTime) {
        this.updateTime = updateTime;
    }

    public String getIsDelete() {
        return isDelete;
    }

    public void setIsDelete(String isDelete) {
        this.isDelete = isDelete;
    }
}